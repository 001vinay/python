"Q.1 Write a program for arithmatic operators"



a=int(input("Enter the First Number: "))#--->Taking input from user
b=int(input("Enter the Second Number: "))#--->Taking input from user
# addition
print ('Sum: ', a + b)#---->Print the output of Addition 

# subtraction
print ('Subtraction: ', a - b) #---->Print the output of Subtraction  

# multiplication
print (' The Multiplication is: ', a * b)#---->Print the output of Multiplication  

# division
print ('Division: ', a / b) #---->Printin the output of Division

# floor division
print ('Floor Division: ', a // b)#---->Printin the output of Floor Division

# modulo
print ('Modulo: ', a % b) #---->Print the output of Modulo

# a to the power b
print ('Power: ', a ** b)#---->Print the output of a to the power b


